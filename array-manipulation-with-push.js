// push berfungsi untuk menyisipkan element di index paling akhir pada array
function manipulationArrayWithPush(){
  var kucing = ['Lucky', 'Gofy', 'Kitty']
  console.log(kucing)

  kucing.push('Sijoy', 'Sitoy')           // menyisipkan element di belakang atau element yang paling terakhir

  return kucing;
}

console.log(manipulationArrayWithPush());
