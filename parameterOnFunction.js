/*
    - parameter adalah sebutan untuk inputan fungsi pada saat pendefenisian fungsi tersebut
    - Dalam sebuah function kita boleh memasukan lebih dari 1 buah parameter
    - Jenis tipe data yang bisa di tampun oleh parameter juga bebas sesuai dengan kebutuhan
*/

function whatIsTheParameters(streetNumber, city, country){
  console.log(streetNumber);
  console.log(city);
  console.log(country);
}
whatIsTheParameters(1123, ['Payakumbuh','Padang','Bukit Tinggi'], 'Indonesia');
