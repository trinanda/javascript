function belajarSwitchCase(){
  var value = 12;

  switch(value){
    case 10:
      console.log('Sangat Bagus');
      break;
    case 7:
      console.log('Baik');
      break;
    case 5:
      console.log('Buruk');
      break;
    default:
      console.log('Data tidak dikenali');
  }
}

belajarSwitchCase()
